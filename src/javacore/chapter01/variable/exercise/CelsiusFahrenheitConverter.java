package javacore.chapter01.variable.exercise;

public class CelsiusFahrenheitConverter {

    public static void main(String[] args) {

        double degreesCelsius = 25.0;

        double toFahrenheit = (degreesCelsius * 1.8) + 32;

        System.out.println(degreesCelsius + "° Celius équivaut à " + toFahrenheit + "° Fahrenheit.");
    }
}
